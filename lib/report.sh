#!/usr/bin/env bash
# ============================================================================
# VulnSage Benchmark — Report Generator
# Generates Markdown summary reports for benchmark runs
# ============================================================================

# This script is sourced by run-benchmark.sh, common.sh is already loaded

generate_report() {
  local spiral="$1"
  local passed="$2"
  local failed="$3"
  local skipped="$4"
  local elapsed="$5"
  local failed_list="$6"

  local total=$((passed + failed + skipped))
  local run_id
  run_id="$(date '+%Y%m%d_%H%M%S')"
  local report_file="${REPORTS_DIR}/benchmark_S${spiral}_${run_id}.md"

  mkdir -p "$REPORTS_DIR"

  cat > "$report_file" <<EOF
# VulnSage Benchmark Report — Spiral ${spiral}

**Run ID**: ${run_id}
**Date**: $(date -u '+%Y-%m-%d %H:%M:%S UTC')
**Duration**: $(timer_format "$elapsed")

## Summary

| Metric | Value |
|--------|-------|
| Total CVEs | ${total} |
| Passed | ${passed} |
| Failed | ${failed} |
| Skipped | ${skipped} |
| Pass Rate | $(( total > 0 ? passed * 100 / total : 0 ))% |

## Matrix Results

EOF

  # Generate 4x4 matrix table
  echo "| | CLI | Library | WebApp | Service |" >> "$report_file"
  echo "|---|:---:|:---:|:---:|:---:|" >> "$report_file"

  for lang in python java go javascript; do
    local row="| **${lang}** |"
    for cat in cli library webapp service; do
      # Count fixtures for this cell
      local cell_fixtures
      cell_fixtures=$(jq -r --arg l "$lang" --arg c "$cat" \
        '.cves[] | select(.language == $l and .category == $c) | .fixture' \
        "$MATRIX_FILE" 2>/dev/null)

      local cell_pass=0
      local cell_total=0

      while IFS= read -r fx; do
        [[ -z "$fx" ]] && continue
        cell_total=$((cell_total + 1))

        # Check if this fixture is in the failed list
        local is_failed=false
        for ff in $failed_list; do
          if [[ "$ff" == "$fx" ]]; then
            is_failed=true
            break
          fi
        done

        if ! $is_failed; then
          cell_pass=$((cell_pass + 1))
        fi
      done <<< "$cell_fixtures"

      if [[ $cell_total -eq 0 ]]; then
        row+=" - |"
      elif [[ $cell_pass -eq $cell_total ]]; then
        row+=" ${cell_pass}/${cell_total} |"
      else
        row+=" ${cell_pass}/${cell_total} |"
      fi
    done
    echo "$row" >> "$report_file"
  done

  # Failed test details
  if [[ -n "$failed_list" ]]; then
    echo "" >> "$report_file"
    echo "## Failed Tests" >> "$report_file"
    echo "" >> "$report_file"

    for fx in $failed_list; do
      local meta_file="${FIXTURES_DIR}/${fx}/meta.json"
      if [[ -f "$meta_file" ]]; then
        local cve project vtype
        cve=$(jq -r '.cve_id // "unknown"' "$meta_file")
        project=$(jq -r '.project // "unknown"' "$meta_file")
        vtype=$(jq -r '.vuln_type // "unknown"' "$meta_file")
        echo "- **${fx}**: ${cve} (${project}) — ${vtype}" >> "$report_file"
      else
        echo "- **${fx}**: (meta.json not found)" >> "$report_file"
      fi
    done
  fi

  echo "" >> "$report_file"
  echo "---" >> "$report_file"
  echo "*Generated by VulnSage Benchmark v1.0.0*" >> "$report_file"

  log_ok "Report saved: ${report_file}"
}
