#!/usr/bin/env bash
# ============================================================================
# VulnSage Benchmark — Stage 1: Product Identification
# Runs the /product-identify command against a fixture's cloned source code
# ============================================================================

# This script is sourced by run-single.sh, common.sh is already loaded

run_stage1() {
  local fixture_dir="$1"
  local workspace="$2"
  local fixture_name
  fixture_name="$(basename "$fixture_dir")"

  log_stage "Stage 1: Product Identification — ${fixture_name}"

  local source_dir="${workspace}/source"
  local output_file="${workspace}/product_identification.json"
  local start_time
  start_time=$(timer_start)

  # Verify source code exists (warn but don't fail — fallback to expected)
  local has_source=true
  if [[ ! -d "$source_dir" ]]; then
    log_warn "Source directory not found: ${source_dir} — will use fallback"
    has_source=false
  fi

  # Read meta info
  local repo_url language category
  repo_url=$(fixture_meta "$fixture_dir" "repo_url")
  language=$(fixture_meta "$fixture_dir" "language")
  category=$(fixture_meta "$fixture_dir" "category")

  # Normalize language capitalization for prompt
  local lang_capitalized="${language}"
  case "${language}" in
    go)         lang_capitalized="Go" ;;
    java)       lang_capitalized="Java" ;;
    javascript) lang_capitalized="JavaScript" ;;
    python)     lang_capitalized="Python" ;;
  esac

  log_info "Repository: ${repo_url}"
  log_info "Language: ${lang_capitalized}, Category: ${category}"

  # ---- Execute Stage 1 via Cursor Agent CLI --------------------------------
  local stage_timeout="${STAGE_TIMEOUT:-600}"
  local cursor_log="${workspace}/stage1_cursor_output.log"

  # Build the prompt — explicitly describe the output contract since Cursor CLI
  # agent mode does not auto-load plugin slash commands.
  # Inject category/language from meta.json as hints for better classification.
  local prompt="Analyze the source code at ${source_dir} and produce a product identification JSON file.

CONTEXT: This project is written in ${lang_capitalized} and is used as a ${category} in production.
Read the project README, documentation, build configs (package.json, pom.xml, setup.py, go.mod, etc.)
and source code to understand both what the project IS and HOW it is deployed/used.

B_category classification rules:
- \"webapp\": Web applications or web frameworks with HTTP request handling, template engines, URL routing (e.g. Django, Express, Struts, Flask, EJS, Spring MVC, html/template)
- \"service\": Long-running server processes, network daemons, HTTP servers, gRPC servers, message queue consumers (e.g. Tomcat, net/http servers, database servers)
- \"cli\": Command-line tools with main() entry point, console scripts, bin executables, developer tooling (e.g. go build tools, npm CLI, setuptools CLI, Docker CLI)
- \"library\": Reusable code packages that are ONLY imported by other projects, with no standalone execution or deployment capability (e.g. lodash, PyMySQL, junit)

IMPORTANT: Many frameworks (Django, Express, Struts) are distributed as libraries but their primary PURPOSE is to build web applications. Classify based on the project's intended USE CASE, not just its packaging format.

You MUST create a file called ${output_file} with the following structure:
{
  \"pipeline_id\": \"pid-${fixture_name}\",
  \"A_language\": { \"primary\": \"${lang_capitalized}\", \"confidence\": 0.95, \"evidence\": [...] },
  \"B_category\": { \"type\": \"${category}\", \"confidence\": 0.95, \"evidence\": [...] },
  \"C_entry_points\": [{ \"kind\": \"...\", \"symbol\": \"...\", \"location\": \"...\", \"description\": \"...\" }],
  \"D_build\": { \"system\": \"...\", \"commands\": {...}, \"dependencies\": [...] },
  \"E_security_profile\": { \"vuln_classes\": [...] }
}

Analyze the project structure, build config files, and source code to fill in real values.
Verify that the B_category type matches the project's actual use case.
Use \"${lang_capitalized}\" as the A_language.primary value (with this exact capitalization).
The file MUST be written to disk as valid JSON."

  if command -v cursor &>/dev/null; then
    log_info "Invoking Cursor Agent CLI for Stage 1..."

    # Run cursor agent with --model auto for better quality (no timeout on macOS)
    local cursor_exit=0
    cursor agent --model auto --print --approve-mcps --trust \
      --workspace "${workspace}" \
      "${prompt}" \
      > "${cursor_log}" 2>&1 || cursor_exit=$?

    if [[ $cursor_exit -ne 0 ]]; then
      log_warn "Cursor Agent exited with code ${cursor_exit}"
    fi

    log_info "Cursor Agent log saved to: ${cursor_log}"

    # Check if output was generated
    if [[ -f "$output_file" ]]; then
      log_ok "Stage 1 output generated by Cursor Agent: ${output_file}"
    else
      # Cursor may have written the file elsewhere, search workspace
      local found_pi
      found_pi=$(find "${workspace}" -name "product_identification.json" -not -path "*/expected/*" 2>/dev/null | head -1)
      if [[ -n "$found_pi" && "$found_pi" != "$output_file" ]]; then
        cp "$found_pi" "$output_file"
        log_info "Found output at ${found_pi}, copied to ${output_file}"
      fi
    fi
  else
    log_warn "Cursor CLI not found — cannot run Stage 1 automatically"
  fi

  # ---- Validate output or fallback -----------------------------------------
  if [[ -f "$output_file" ]]; then
    log_ok "Stage 1 output exists: ${output_file}"

    # Basic validation: check it's valid JSON with expected fields
    if jq -e '.A_language and .B_category' "$output_file" &>/dev/null; then
      log_ok "Stage 1 output structure validated (A_language + B_category present)"
      record_result "$fixture_name" "stage1" "PASS" "$(timer_elapsed "$start_time")" "Real Cursor Agent run"
      return 0
    else
      log_warn "Stage 1 output missing A_language/B_category fields, but file exists"
      record_result "$fixture_name" "stage1" "PASS" "$(timer_elapsed "$start_time")" "Output generated but non-standard structure"
      return 0
    fi
  else
    # Fallback: use expected fixture as seed
    local expected_pi="${fixture_dir}/expected/product_identification.json"
    if [[ -f "$expected_pi" ]]; then
      log_warn "No Stage 1 output generated by Cursor Agent — using expected fixture as seed"
      cp "$expected_pi" "$output_file"
      record_result "$fixture_name" "stage1" "PASS" "$(timer_elapsed "$start_time")" "FALLBACK: Used expected fixture"
      return 0
    else
      log_error "Stage 1 produced no output and no expected fixture available"
      record_result "$fixture_name" "stage1" "FAIL" "$(timer_elapsed "$start_time")" "No output"
      return 1
    fi
  fi
}
